/*
 * TA_MFI - Money Flow Index
 * 
 * Input  = High, Low, Close, Volume
 * Output = TA_Real
 * 
 * Optional Parameters
 * -------------------
 * optInTimePeriod_0:(From 1 to TA_INTEGER_MAX)
 *    Number of period
 * 
 * 
 */

TA_RetCode TA_MFI( TA_Integer    startIdx,
                   TA_Integer    endIdx,
                   const TA_Real inHigh_0[],
                   const TA_Real inLow_0[],
                   const TA_Real inClose_0[],
                   const TA_Integer inVolume_0[],
                   TA_Integer    optInTimePeriod_0, /* From 1 to TA_INTEGER_MAX */
                   TA_Integer   *outBegIdx,
                   TA_Integer   *outNbElement,
                   TA_Real       outReal_0[] )
/**** END GENCODE SECTION 2 - DO NOT DELETE THIS LINE ****/
{
	/* insert local variable here */
   TA_Real posSumMF, negSumMF, prevValue;
   TA_Real tempValue1, tempValue2;
   int lookbackTotal, outIdx, i, today;

   CIRCBUF_PROLOG( mflow, MoneyFlow, 50 ); /* Id, Type, Static Size */
   
/**** START GENCODE SECTION 3 - DO NOT DELETE THIS LINE ****/

#ifndef TA_FUNC_NO_RANGE_CHECK

   /* Validate the requested output range. */
   if( startIdx < 0 )
      return TA_OUT_OF_RANGE_START_INDEX;
   if( (endIdx < 0) || (endIdx < startIdx))
      return TA_OUT_OF_RANGE_END_INDEX;

   /* Validate the parameters. */
   /* Verify required price component. */
   if(!inHigh_0||!inLow_0||!inClose_0||!inVolume_0)
      return TA_BAD_PARAM;

   /* min/max are checked for optInTimePeriod_0. */
   if( optInTimePeriod_0 == TA_INTEGER_DEFAULT )
      optInTimePeriod_0 = 14;
   else if( (optInTimePeriod_0 < 1) || (optInTimePeriod_0 > 2147483647) )
      return TA_BAD_PARAM;

   if( outReal_0 == NULL )
      return TA_BAD_PARAM;

#endif /* TA_FUNC_NO_RANGE_CHECK */

/**** END GENCODE SECTION 3 - DO NOT DELETE THIS LINE ****/

   /* Insert TA function code here. */

   CIRCBUF_CONSTRUCT( mflow, MoneyFlow, optInTimePeriod_0 );

   *outBegIdx    = 0;
   *outNbElement = 0;
   
   /* Adjust startIdx to account for the lookback period. */
   lookbackTotal = optInTimePeriod_0 + TA_Globals.unstablePeriod[TA_FUNC_UNST_MFI];

   if( startIdx < lookbackTotal )
      startIdx = lookbackTotal;

   /* Make sure there is still something to evaluate. */
   if( startIdx > endIdx )
      return TA_SUCCESS;

   outIdx = 0; /* Index into the output. */

   /* Accumulate the positive and negative money flow
    * among the initial period.
    */
   today = startIdx-lookbackTotal;
   prevValue = (inHigh_0[today]+inLow_0[today]+inClose_0[today])/3.0;

   posSumMF = 0.0;
   negSumMF = 0.0;
   today++;
   for( i=optInTimePeriod_0; i > 0; i-- )
   {
      tempValue1 = (inHigh_0[today]+inLow_0[today]+inClose_0[today])/3.0;
      tempValue2 = tempValue1 - prevValue;
      prevValue  = tempValue1;
      tempValue1 *= inVolume_0[today++];
      if( tempValue2 < 0 )
      {
         mflow[addIdx_mflow].negative = tempValue1;
         negSumMF += tempValue1;
         mflow[addIdx_mflow].positive = 0.0;
      }
      else
      {
         mflow[addIdx_mflow].positive = tempValue1;
         posSumMF += tempValue1;
         mflow[addIdx_mflow].negative = 0.0;
      }

      CIRCBUF_NEXT(mflow);
   }
   
   /* The following two equations are equivalent:
    *    MFI = 100 - (100 / 1 + (posSumMF/negSumMF))
    *    MFI = 100 * (posSumMF/(posSumMF+negSumMF))
    * The second equation is used here for speed optimization.
    */
   if( today > startIdx )
      outReal_0[outIdx++] = 100.0*(posSumMF/(posSumMF+negSumMF));
   else
   {
      /* Skip the unstable period. Do the processing 
       * but do not write it in the output.
       */   
      while( today < startIdx )
      {
         posSumMF -= mflow[getIdx_mflow].positive;
         negSumMF -= mflow[getIdx_mflow].negative;

         tempValue1 = (inHigh_0[today]+inLow_0[today]+inClose_0[today])/3.0;
         tempValue2 = tempValue1 - prevValue;
         prevValue  = tempValue1;
         tempValue1 *= inVolume_0[today++];
         if( tempValue2 < 0 )
         {
            mflow[addIdx_mflow].negative = tempValue1;
            negSumMF += tempValue1;
            mflow[addIdx_mflow].positive = 0.0;
         }
         else
         {
            mflow[addIdx_mflow].positive = tempValue1;
            posSumMF += tempValue1;
            mflow[addIdx_mflow].negative = 0.0;
         }

         CIRCBUF_NEXT(mflow);
      }
   }

   /* Unstable period skipped... now continue
    * processing if needed.
    */
   while( today <= endIdx )
   {
      posSumMF -= mflow[getIdx_mflow].positive;
      negSumMF -= mflow[getIdx_mflow].negative;

      tempValue1 = (inHigh_0[today]+inLow_0[today]+inClose_0[today])/3.0;
      tempValue2 = tempValue1 - prevValue;
      prevValue  = tempValue1;
      tempValue1 *= inVolume_0[today++];
      if( tempValue2 < 0 )
      {
         mflow[addIdx_mflow].negative = tempValue1;
         negSumMF += tempValue1;
         mflow[addIdx_mflow].positive = 0.0;
         outReal_0[outIdx++] = tempValue1;
      }
      else
      {
         mflow[addIdx_mflow].positive = tempValue1;
         posSumMF += tempValue1;
         mflow[addIdx_mflow].negative = 0.0;
      }

      //outReal_0[outIdx++] = 100.0*(posSumMF/(posSumMF+negSumMF));

      CIRCBUF_NEXT(mflow);
   }

   CIRCBUF_DESTROY(mflow);

   *outBegIdx = startIdx;
   *outNbElement = outIdx;

   return TA_SUCCESS;
}

