TA_RetCode TA_CCI( TA_Integer    startIdx,
                   TA_Integer    endIdx,
                   const TA_Real inHigh_0[],
                   const TA_Real inLow_0[],
                   const TA_Real inClose_0[],
                   TA_Integer    optInTimePeriod_0, /* From 5 to TA_INTEGER_MAX */
                   TA_Integer   *outBegIdx,
                   TA_Integer   *outNbElement,
                   TA_Real       outReal_0[] )
/**** END GENCODE SECTION 2 - DO NOT DELETE THIS LINE ****/
{

	/* insert local variable here */
   TA_Real periodTotal, tempReal, tempReal2, theAverage, lastValue;
   int i, j, outIdx, trailingIdx, headingIdx, lookbackTotal;

   /* This ptr will points on a circular buffer of
    * at least "optInTimePeriod_0" element.
    */
   TA_Real *circBuffer;

   /* To avoid a memory allocation, use a small buffer
    * taken on the stack when the period is smaller
    * or equal to LOCAL_BUFFER_SIZE.
    */
   #define LOCAL_BUFFER_SIZE 30
   TA_Real localBuffer[LOCAL_BUFFER_SIZE];

/**** START GENCODE SECTION 3 - DO NOT DELETE THIS LINE ****/

#ifndef TA_FUNC_NO_RANGE_CHECK

   /* Validate the requested output range. */
   if( startIdx < 0 )
      return TA_OUT_OF_RANGE_START_INDEX;
   if( (endIdx < 0) || (endIdx < startIdx))
      return TA_OUT_OF_RANGE_END_INDEX;

   /* Validate the parameters. */
   /* Verify required price component. */
   if(!inHigh_0||!inLow_0||!inClose_0)
      return TA_BAD_PARAM;

   /* min/max are checked for optInTimePeriod_0. */
   if( optInTimePeriod_0 == TA_INTEGER_DEFAULT )
      optInTimePeriod_0 = 14;
   else if( (optInTimePeriod_0 < 5) || (optInTimePeriod_0 > 2147483647) )
      return TA_BAD_PARAM;

   if( outReal_0 == NULL )
      return TA_BAD_PARAM;

#endif /* TA_FUNC_NO_RANGE_CHECK */

/**** END GENCODE SECTION 3 - DO NOT DELETE THIS LINE ****/

   /* Insert TA function code here. */

   /* Identify the minimum number of price bar needed
    * to calculate at least one output.
    */
   lookbackTotal = (optInTimePeriod_0-1);

   /* Move up the start index if there is not
    * enough initial data.
    */
   if( startIdx < lookbackTotal )
      startIdx = lookbackTotal;

   /* Make sure there is still something to evaluate. */
   if( startIdx > endIdx )
   {
      *outBegIdx = 0;
      *outNbElement = 0;
      return TA_SUCCESS;
   }

   /* Allocate a circular buffer equal to the requested
    * period.
    */
   if( optInTimePeriod_0 > LOCAL_BUFFER_SIZE )
   {
      circBuffer = TA_Malloc( optInTimePeriod_0*sizeof(TA_Real) );
      if( !circBuffer )
         return TA_ALLOC_ERR;
   }
   else
      circBuffer = localBuffer;
  
   headingIdx = trailingIdx = 0;

   /* Do the MA calculation using tight loops. */

   /* Add-up the initial period, except for the last value. 
    * Fill up the circular buffer at the same time.
    */
   periodTotal = 0;   
   i=startIdx-lookbackTotal;
   if( optInTimePeriod_0 > 1 )
   {
      while( i < startIdx )
      {
         lastValue = (inHigh_0[i]+inLow_0[i]+inClose_0[i])/3;
         i++;
         periodTotal += lastValue;
         circBuffer[headingIdx++] = lastValue; 
      }
   }

   /* Proceed with the calculation for the requested range.
    * Note that this algorithm allows the inReal_0 and
    * outReal_0 to be the same buffer.
    */
   outIdx = 0;
   do
   {
      /* Calculate the average for the whole period. */
      lastValue = (inHigh_0[i]+inLow_0[i]+inClose_0[i])/3;
      circBuffer[headingIdx] = lastValue;
      periodTotal += lastValue;
      tempReal  = periodTotal;
      periodTotal -= circBuffer[trailingIdx];
      theAverage = tempReal / optInTimePeriod_0;

      /* Do the summation of the ABS(TypePrice-average)
       * for the whole period.
       */
      tempReal2 = 0;
      for( j=0; j < optInTimePeriod_0; j++ )
         tempReal2 += fabs(circBuffer[j]-theAverage);

      /* And finally, the CCI... */
      tempReal2 /= optInTimePeriod_0;
      tempReal = lastValue-theAverage;

      // printf( "%g,%g,[%g]", lastValue, theAverage, tempReal2 );
      if( (tempReal != 0.0) && (tempReal2 != 0.0) )
         outReal_0[outIdx++] = tempReal/(0.015*tempReal2);
      else
         outReal_0[outIdx++] = 0.0;
      
      /* Move forward the circular buffer indexes. */
      trailingIdx++;
      if( trailingIdx == 1 )
         headingIdx = 0;
      else 
      {
         if( trailingIdx == optInTimePeriod_0 )
            trailingIdx = 0;
         headingIdx++;
      }

      i++;
   } while( i <= endIdx );

   /* All done. Indicate the output limits and return. */
   *outNbElement = outIdx;
   *outBegIdx    = startIdx;

   /* Free the circular buffer if it was dynamically allocated. */
   if( circBuffer != localBuffer )
      TA_Free(  circBuffer );

   return TA_SUCCESS;
}

